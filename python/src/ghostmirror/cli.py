def main(**kwargs):
    """
    GhostMirror: A Python tool to rank Arch Linux pacman mirrors.

    This CLI allows users to fetch mirror lists, filter them by various criteria
    (country, protocol), test mirror speeds, compare package freshness, and
    generate ranked mirror lists for use with pacman.
    """
    # Create a configuration dictionary from all Click options
    config = {}
    for key, value in kwargs.items():
        config[key] = value

    # Process path-related options to expand and resolve them
    if config.get("mirrorfile"):
        config["mirrorfile"] = path_explode(config["mirrorfile"])

    if config.get("list_output_file") and config["list_output_file"].lower() != "stdout":
        config["list_output_file"] = path_explode(config["list_output_file"])

    # If progress_colors is true, progress should also be implicitly true
    if config.get("progress_colors"):
        config["progress"] = True
    
    # Rename CLI option names to match expected keys in core.py's config dict where necessary
    # This is done if 'param_name' in @click.option(...) differs from the desired config key.
    # Example: if @click.option had 'country_filters_cli' but core.py expects 'country_filter'
    # Here, most were aligned by changing the 'dest' (first string arg after flags) in @click.option.
    # For example, @click.option('--country', '-c', 'country_filter', ...) ensures kwargs['country_filter']
    # No explicit remapping needed here if dest names in @click.option match core.py's expectations.
    # I've already aligned 'country' to 'country_filter', 'uncommented' to 'uncommented_only',
    # and 'max_list_mirrors' to 'max_list' in the @click.option definitions.

    # Call the core processing function with the prepared configuration
    core.process_mirrors(config)

if __name__ == '__main__':
    main()
