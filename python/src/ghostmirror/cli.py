def path_home() -> pathlib.Path:
    """
    Returns the user's home directory as a `pathlib.Path` object.

    This is a simple wrapper around `pathlib.Path.home()` for convenience.

    Returns:
        pathlib.Path: The path to the user's home directory.
    """
    return pathlib.Path.home()

def path_explode(path_str: str | None) -> str | None:
    """
    Expands and resolves a path string.

    Handles common path idioms:
    - If `path_str` is None or empty, returns None.
    - If `path_str` starts with "~/", it expands "~" to the user's home directory.
    - Otherwise, it treats `path_str` as a relative or absolute path and resolves it
      to an absolute path.
    - Normalizes path separators to forward slashes ("/").

    Args:
        path_str (str | None): The path string to expand. Can be None.

    Returns:
        str | None: The expanded and resolved absolute path as a string with forward
                    slashes, or None if the input was None or empty.
    """
    if not path_str: # Handle None or empty string input
        return None
    if path_str.startswith('~/'):
        # Expand "~/" to the user's home directory
        return str(pathlib.Path.home() / path_str[2:]).replace('\\', '/')
    # Resolve other paths (relative or absolute) to an absolute path
    return str(pathlib.Path(path_str).resolve()).replace('\\', '/')

